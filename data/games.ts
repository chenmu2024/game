export const getGameBySlug = (slug: string) => {
  return allGames.find((game) => game.slug === slug)
}

export const getRelatedGames = (gameId: number, limit = 3) => {
  const game = allGames.find((g) => g.id === gameId)
  if (!game || !game.relatedGames) return []

  return game.relatedGames
    .map((id) => allGames.find((g) => g.id === id))
    .filter(Boolean)
    .slice(0, limit)
}

export const getGamesByCategory = (category: string, limit?: number) => {
  const games = allGames.filter(
    (game) =>
      game.category.toLowerCase() === category.toLowerCase() ||
      game.tags.some((tag) => tag.toLowerCase() === category.toLowerCase()),
  )

  return limit ? games.slice(0, limit) : games
}

export const getPopularGames = (limit = 4) => {
  return [...allGames]
    .sort((a, b) => {
      const playsA = Number.parseInt(a.plays.replace(/[^0-9]/g, ""))
      const playsB = Number.parseInt(b.plays.replace(/[^0-9]/g, ""))
      return playsB - playsA
    })
    .slice(0, limit)
}

export const getFeaturedGame = () => {
  return allGames.find((game) => game.featured)
}

export const getLatestGames = (limit = 4) => {
  return [...allGames]
    .sort((a, b) => new Date(b.releaseDate).getTime() - new Date(a.releaseDate).getTime())
    .slice(0, limit)
}

export const searchGames = (query: string) => {
  const searchTerms = query.toLowerCase().split(" ")

  return allGames.filter((game) => {
    const searchableText = [
      game.title,
      game.description,
      game.longDescription,
      game.category,
      ...game.tags,
      ...(game.seoKeywords || []),
    ]
      .join(" ")
      .toLowerCase()

    return searchTerms.every((term) => searchableText.includes(term))
  })
}

